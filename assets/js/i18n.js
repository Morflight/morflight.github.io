/* ============================================================
   i18n — language switcher
   Reads pre-translated JSON files generated by GitHub Actions.
   Exposes window.i18n for use by other scripts.
   ============================================================ */

const LANGUAGES  = ['en', 'fr', 'de', 'es', 'pt', 'ru'];
const DEFAULT    = 'en';

let _translations = {};
let _current      = DEFAULT;
let _cache        = {};

/* -- Lookup ------------------------------------------------ */

function t(key) {
  const parts = key.split('.');
  let node = _translations;
  for (const p of parts) {
    if (node == null) return null;
    node = node[p];
  }
  return (typeof node === 'string') ? node : null;
}

/* -- DOM apply --------------------------------------------- */

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = t(el.dataset.i18n);
    if (val) el.textContent = val;
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const val = t(el.dataset.i18nPlaceholder);
    if (val) el.placeholder = val;
  });
}

/* -- Load & switch ----------------------------------------- */

async function setLang(lang) {
  if (!LANGUAGES.includes(lang)) lang = DEFAULT;
  _current = lang;
  localStorage.setItem('lang', lang);

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  if (_cache[lang]) {
    _translations = _cache[lang];
  } else {
    try {
      const res = await fetch(`/i18n/${lang}.json?t=${Date.now()}`);
      _translations = res.ok ? await res.json() : {};
      _cache[lang] = _translations;
    } catch (_) {
      _translations = {};
    }
  }

  applyTranslations();

  /* show AI disclaimer only for non-English languages */
  document.querySelectorAll('.footer__ai-note').forEach(el => {
    el.hidden = (lang === DEFAULT);
  });

  /* re-render dynamic content on pages that have it */
  if (typeof applyFilters === 'function') applyFilters();
}

/* -- Boot -------------------------------------------------- */

const _saved = localStorage.getItem('lang') || DEFAULT;

window.i18n = { t, applyTranslations, setLang, get current() { return _current; } };

/* Attach switcher buttons (runs after DOM is ready) */
document.addEventListener('DOMContentLoaded', () => {
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => setLang(btn.dataset.lang));
  });
  setLang(_saved);
});
